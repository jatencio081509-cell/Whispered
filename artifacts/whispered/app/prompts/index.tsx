interface PromptRecord {
  id: string;
  date: string;
  prompt: string;
  user1_id: string;
  user2_id: string | null;
  user1_answer: string | null;
  user2_answer: string | null;
  revealed: boolean;
  created_at: string;
  updated_at: string;
}

function getTodayKey() {
  const now = new Date();
  const year = now.getFullYear();
  const month = String(now.getMonth() + 1).padStart(2, '0');
  const day = String(now.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function getDailyPrompt(): string {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) /
      (1000 * 60 * 60 * 24),
  );
  return PROMPTS[dayOfYear % PROMPTS.length];
}

export default function PromptsScreen() {
  const colors = useColors();
  const insets = useSafeAreaInsets();
  const router = useRouter();
  const { user } = useUser();
  const [todayRecord, setTodayRecord] = useState<PromptRecord | null>(null);
  const [myAnswer, setMyAnswer] = useState("");
  const [archive, setArchive] = useState<PromptRecord[]>([]);
  const [submitted, setSubmitted] = useState(false);
  const [loading, setLoading] = useState(true);
  const [showNavigationDrawer, setShowNavigationDrawer] = useState(false);

  const topPad = Platform.OS === "web" ? insets.top + 67 : insets.top;
  const todayKey = getTodayKey();
  const todayPrompt = getDailyPrompt();
  const myUserId = user?.id;
  const partnerUserId = user?.unsafeMetadata?.partner_user_id as string | undefined;
  const partnerName = user?.unsafeMetadata?.partnerName as string | undefined;

  // Generate a consistent couple ID from the two user IDs
  const generateCoupleId = (userId1: string, userId2: string) => {
    const sorted = [userId1, userId2].sort();
    return `${sorted[0]}-${sorted[1]}`;
  };

  const effectiveCoupleId = (myUserId && partnerUserId) 
    ? generateCoupleId(myUserId, partnerUserId)
    : null;

  const fetchTodayPrompt = async () => {
    if (!myUserId) return;
    
    try {
      const { data, error } = await supabase
        .from('prompts')
        .select('*')
        .eq('date', todayKey)
        .or(`user1_id.eq.${myUserId},user2_id.eq.${myUserId}`)
        .single();

      if (error && error.code !== 'PGRST116') {
        console.error('Error fetching today prompt:', error);
        return;
      }

      if (data) {
        setTodayRecord(data);
        const myAnswer = data.user1_id === myUserId ? data.user1_answer : data.user2_answer;
        setMyAnswer(myAnswer || '');
        setSubmitted(!!myAnswer);
      }
    } catch (err) {
      console.error('Failed to fetch today prompt:', err);
    } finally {
      setLoading(false);
    }
  };

  const fetchArchive = async () => {
    if (!myUserId) return;
    
    try {
      const { data, error } = await supabase
        .from('prompts')
        .select('*')
        .not('date', 'eq', todayKey)
        .or(`user1_id.eq.${myUserId},user2_id.eq.${myUserId}`)
        .order('date', { ascending: false })
        .limit(20);

      if (error) {
        console.error('Error fetching archive:', error);
        return;
      }

      setArchive(data || []);
    } catch (err) {
      console.error('Failed to fetch archive:', err);
    }
  };

  useEffect(() => {
    if (myUserId) {
      fetchTodayPrompt();
      fetchArchive();

      // Set up real-time subscription
      const channel = supabase
        .channel('prompts_changes')
        .on(
          'postgres_changes',
          {
            event: '*',
            schema: 'public',
            table: 'prompts',
            filter: `date=eq.${todayKey}`,
          },
          () => {
            fetchTodayPrompt();
          }
        )
        .subscribe();

      return () => {
        supabase.removeChannel(channel);
      };
    }
  }, [myUserId, todayKey]);

  const submitAnswer = async () => {
    if (!myAnswer.trim() || !myUserId) return;
    Haptics.notificationAsync(Haptics.NotificationFeedbackType.Success);

    try {
      if (todayRecord) {
        // Update existing record
        const updateData: any = {
          updated_at: new Date().toISOString(),
        };
        
        if (todayRecord.user1_id === myUserId) {
          updateData.user1_answer = myAnswer.trim();
        } else if (todayRecord.user2_id === myUserId) {
          updateData.user2_answer = myAnswer.trim();
        }

        const { error } = await supabase
          .from('prompts')
          .update(updateData)
          .eq('id', todayRecord.id);

        if (error) {
          console.error('Error updating prompt:', error);
          alert('Failed to save answer: ' + error.message);
          return;
        }
      } else {
        // Create new record
        const { error } = await supabase.from('prompts').insert({
          date: todayKey,
          prompt: todayPrompt,
          user1_id: myUserId,
          user2_id: partnerUserId || null,
          user1_answer: myAnswer.trim(),
          user2_answer: null,
          revealed: false,
        });

        if (error) {
          console.error('Error creating prompt:', error);
          alert('Failed to save answer: ' + error.message);
          return;
        }
      }

      setSubmitted(true);
      await fetchTodayPrompt();
    } catch (err) {
      console.error('Failed to submit answer:', err);
      alert('Failed to save answer');
    }
  };

  const revealAnswers = async () => {
    if (!todayRecord || !myUserId) return;
    Haptics.notificationAsync(Haptics.NotificationFeedbackType.Success);

    try {
      const { error } = await supabase
        .from('prompts')
        .update({ revealed: true, updated_at: new Date().toISOString() })
        .eq('id', todayRecord.id);

      if (error) {
        console.error('Error revealing answers:', error);
        alert('Failed to reveal answers: ' + error.message);
        return;
      }

      await fetchTodayPrompt();
    } catch (err) {
      console.error('Failed to reveal answers:', err);
      alert('Failed to reveal answers');
    }
  };

  const formatDate = (d: string) =>
    new Date(d).toLocaleDateString(undefined, {
      month: "short",
      day: "numeric",
    });

  return (
    <KeyboardAvoidingView
      behavior={Platform.OS === "ios" ? "padding" : "height"}
      style={{ flex: 1, backgroundColor: colors.background }}
    >
      <View style={[styles.container, { backgroundColor: colors.background }]}>
        <ThemeBackground>

        <View
          style={[
            styles.header,
            { paddingTop: topPad + 12, borderBottomColor: colors.border },
          ]}
        >
          <Text style={[styles.headerTitle, { color: colors.text }]}>
            Daily Prompts
          </Text>
          <Pressable onPress={() => setShowNavigationDrawer(true)}>
            <Feather name="menu" size={24} color={colors.text} />
          </Pressable>
        </View>

        <ScrollView
          contentContainerStyle={[
            styles.list,
            { paddingBottom: insets.bottom + 100 },
          ]}
          showsVerticalScrollIndicator={false}
        >
          {loading ? (
            <View style={styles.empty}>
              <Text style={[styles.emptyTitle, { color: colors.text }]}>Loading...</Text>
            </View>
          ) : (
            <>
              {/* Today's Prompt */}
              <LinearGradient
                colors={[`${colors.accent}30`, `${colors.primary}20`]}
                style={[styles.promptCard, { borderColor: `${colors.accent}40` }]}
                start={{ x: 0, y: 0 }}
                end={{ x: 1, y: 1 }}
              >
                <View style={styles.promptBadge}>
                  <View
                    style={[
                      styles.promptDot,
                      { backgroundColor: colors.primary },
                    ]}
                  />
                  <Text style={[styles.promptBadgeText, { color: colors.primary }]}>
                    Today&apos;s prompt
                  </Text>
                </View>
                <Text style={[styles.promptText, { color: colors.text }]}>
                  {todayPrompt}
                </Text>
              </LinearGradient>

              {!submitted ? (
                <View style={styles.answerSection}>
                  <Text style={[styles.answerLabel, { color: colors.mutedForeground }]}>
                    Your answer
                  </Text>
                  <TextInput
                    style={[
                      styles.answerInput,
                      {
                        backgroundColor: colors.card,
                        borderColor: colors.border,
                        color: colors.text,
                      },
                    ]}
                    value={myAnswer}
                    onChangeText={setMyAnswer}
                    placeholder="Write your honest answer..."
                    placeholderTextColor={colors.mutedForeground}
                    multiline
                    textAlignVertical="top"
                  />
                  <Pressable
                    style={({ pressed }) => [
                      styles.submitBtn,
                      {
                        backgroundColor: colors.primary,
                        opacity: !myAnswer.trim() || pressed ? 0.7 : 1,
                      },
                    ]}
                    onPress={submitAnswer}
                    disabled={!myAnswer.trim()}
                  >
                    <Feather name="send" size={16} color={colors.primaryForeground} />
                    <Text
                      style={[styles.submitText, { color: colors.primaryForeground }]}
                    >
                      Submit answer
                    </Text>
                  </Pressable>
                </View>
              ) : todayRecord && !todayRecord.revealed ? (
                <View
                  style={[
                    styles.submittedCard,
                    { backgroundColor: colors.card, borderColor: colors.border },
                  ]}
                >
                  <View style={styles.submittedHeader}>
                    <Feather name="check-circle" size={16} color={colors.success} />
                    <Text style={[styles.submittedTitle, { color: colors.success }]}>
                      Your answer saved
                    </Text>
                  </View>
                  <Text style={[styles.myAnswerText, { color: colors.text }]}>
                    {myAnswer}
                  </Text>
                  <Text
                    style={[styles.waitingText, { color: colors.mutedForeground }]}
                  >
                    {partnerUserId && todayRecord.user1_answer && todayRecord.user2_answer
                      ? "Both partners have answered!"
                      : "Waiting for your partner to answer..."}
                  </Text>
                  {partnerUserId && todayRecord.user1_answer && todayRecord.user2_answer && (
                    <Pressable
                      style={[styles.revealBtn, { backgroundColor: colors.accent }]}
                      onPress={revealAnswers}
                    >
                      <Feather name="eye" size={16} color={colors.primaryForeground} />
                      <Text style={[styles.revealText, { color: colors.primaryForeground }]}>
                        Reveal both answers
                      </Text>
                    </Pressable>
                  )}
                </View>
              ) : todayRecord && todayRecord.revealed ? (
                <View
                  style={[
                    styles.revealedCard,
                    { backgroundColor: colors.card, borderColor: colors.border },
                  ]}
                >
                  <View style={styles.revealedHeader}>
                    <Feather name="eye" size={16} color={colors.accent} />
                    <Text style={[styles.revealedTitle, { color: colors.accent }]}>
                      Both answers revealed
                    </Text>
                  </View>
                  <View style={styles.answerSection}>
                    <Text style={[styles.answerLabel, { color: colors.primary }]}>
                      Your answer
                    </Text>
                    <Text style={[styles.revealedAnswerText, { color: colors.text }]}>
                      {myAnswer}
                    </Text>
                  </View>
                  {partnerUserId && (
                    <View style={styles.answerSection}>
                      <Text style={[styles.answerLabel, { color: colors.accent }]}>
                        {partnerName || "Partner"}'s answer
                      </Text>
                      <Text style={[styles.revealedAnswerText, { color: colors.text }]}>
                        {todayRecord.user1_id === myUserId ? todayRecord.user2_answer : todayRecord.user1_answer}
                      </Text>
                    </View>
                  )}
                </View>
              ) : null}

              {/* Archive */}
              {archive.length > 0 ? (
                <>
                  <Text
                    style={[styles.archiveTitle, { color: colors.mutedForeground }]}
                  >
                    Past prompts
                  </Text>
                  {archive.map((a) => (
                    <View
                      key={a.id}
                      style={[
                        styles.archiveCard,
                        { backgroundColor: colors.card, borderColor: colors.border },
                      ]}
                    >
                      <Text
                        style={[
                          styles.archiveDate,
                          { color: colors.mutedForeground },
                        ]}
                      >
                        {formatDate(a.date)}
                      </Text>
                      <Text
                        style={[styles.archivePrompt, { color: colors.text }]}
                        numberOfLines={2}
                      >
                        {a.prompt}
                      </Text>
                      {a.revealed ? (
                        <Text
                          style={[
                            styles.archiveAnswer,
                            { color: colors.mutedForeground },
                          ]}
                          numberOfLines={2}
                        >
                          Both answers revealed
                        </Text>
                      ) : (
                        <Text
                          style={[
                            styles.archiveAnswer,
                            { color: colors.mutedForeground },
                          ]}
                          numberOfLines={2}
                        >
                          {a.user1_id === myUserId ? "You answered" : "Partner answered"}
                        </Text>
                      )}
                    </View>
                  ))}
                </>
              ) : null}
            </>
          )}
        </ScrollView>

        <NavigationDrawer
          visible={showNavigationDrawer}
          onClose={() => setShowNavigationDrawer(false)}
        />
        </ThemeBackground>
      </View>
    </KeyboardAvoidingView>
  );
}

